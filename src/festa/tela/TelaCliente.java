    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Botão ADICIONAR
        try {
            if (jTextField6.getText().isEmpty()) {
                Cliente cliente = new Cliente();

                String nome = jTextField1.getText();
                String cpf = jFormattedTextField1.getText();
                String telefone = jTextField2.getText().trim();
                String email = jTextField3.getText();
                String estado = (String) jComboBox1.getSelectedItem();
                String cidade = jTextField4.getText();
                String logradouro = jTextField5.getText();
                SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
                Date data = format.parse(jFormattedTextField2.getText());

                if (!nome.isEmpty() && cpf.length() == 14 && !telefone.isEmpty()) {
                    cliente.set(nome, cpf, telefone, email, estado, cidade, logradouro, new java.sql.Date(data.getTime()), 
                            true);
                    ClienteDAO dao = new ClienteDAO();
                    dao.adicionarCliente(cliente);
                    limparCampos();
                    JOptionPane.showMessageDialog(null, "Cliente adicionado com sucesso");
                } else {
                    JOptionPane.showMessageDialog(null, "Preencher os campos obrigatórios");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Não foi possíevel cadastrar cliente, campo ID preenchido");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Botão EDITAR
        try {
            if (!jTextField6.getText().isEmpty()) {
                Cliente cliente = new Cliente();
                String nome = jTextField1.getText();
                String cpf = jFormattedTextField1.getText();
                String telefone = jTextField2.getText().trim();
                String email = jTextField3.getText();
                String estado = (String) jComboBox1.getSelectedItem();
                String cidade = jTextField4.getText();
                String logradouro = jTextField5.getText();
                int idCliente = Integer.parseInt(jTextField6.getText());
                SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
                Date data = format.parse(jFormattedTextField2.getText());
                if (!nome.isEmpty() && cpf.length() == 14 && !telefone.isEmpty()) {
                    cliente.set(nome, cpf, telefone, email, estado, cidade, logradouro, new java.sql.Date(data.getTime()),
                    true);
                    cliente.setIdCliente(idCliente);
                    ClienteDAO dao = new ClienteDAO();
                    dao.alterarCliente(cliente);    
                    limparCampos();
                    JOptionPane.showMessageDialog(null, "Cliente alterado com sucesso");
                } else {
                    JOptionPane.showMessageDialog(null, "Preencher os campos obrigatórios");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Necessário selecionar uma linha");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

