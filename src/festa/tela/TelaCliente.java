    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // Botão CONSULTAR
        try {
            String nome = jTextField1.getText();
            // String cpf = jFormattedTextField1.getText();
            // String telefone = jTextField2.getText().trim();
            // String email = jTextField3.getText();
            //String estado = (String) jComboBox1.getSelectedItem();
            //String cidade = jTextField4.getText();
            //String logradouro = jTextField5.getText();
            //SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
            //Date data = format.parse(jFormattedTextField2.getText());
            String idCliente = jTextField6.getText();
            ArrayList<Cliente> clientes = new ArrayList<>();

            ClienteDAO dao = new ClienteDAO();

            if (!nome.isEmpty()) {
                clientes = dao.consultarClientes(nome);

            } else if (!idCliente.isEmpty()) {
                int idClienteNum = Integer.parseInt(idCliente);
                Cliente cliente = dao.getCliente(idClienteNum);
                clientes.add(cliente);

            } else {
                clientes = dao.listarClientes();
            }

            modelo.setNumRows(0);

            for (Cliente cliente : clientes) {
                Object[] Dados = {cliente.getIdCliente(), cliente.getNome(),
                    cliente.getCpf(), cliente.getTelefone(), cliente.getEstado(), cliente.getCidade()};
                modelo.addRow(Dados);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Somente números no campo ID");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

