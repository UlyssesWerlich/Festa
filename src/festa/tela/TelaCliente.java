    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() != -1) {
            int idContato = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
            ClienteDAO dao = new ClienteDAO();
            Cliente cliente = dao.getCliente(idContato);
            jTextField1.setText(cliente.getNome()); //NOME
            jFormattedTextField1.setText(cliente.getCpf()); //CPF
            jTextField2.setText(cliente.getTelefone()); //TELEFONE
            jTextField3.setText(cliente.getEmail()); //EMAIL
            jComboBox1.setSelectedItem(cliente.getEstado()); //ESTADO
            jTextField4.setText(cliente.getCidade()); //CIDADE
            jTextField5.setText(cliente.getLogradouro()); //LOGRADOURO
            jFormattedTextField2.setText(String.valueOf(cliente.getDataNascimento())); //DATA DE NASCIMENTO 
            jTextField6.setText(String.valueOf(cliente.getIdCliente())); //ID CLIENTE 
            jTextField6.setEditable(false);
        }
    }//GEN-LAST:event_jTable1MouseClicked


