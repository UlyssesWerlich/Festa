    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        // BOT√ÉO ADICIONAR
        try {
            if (jTextField2.getText().isEmpty()) {
                Item item = new Item();

                String nome = jTextField1.getText();
                Double preco = Double.parseDouble(jTextField3.getText());
                String descricao = jTextArea1.getText();

                if (!nome.isEmpty()) {
                    item.set(nome, descricao, preco, true);
                    ItemDAO dao = new ItemDAO();
                    dao.adicionarItem(item);
                    limparCampos();
                    JOptionPane.showMessageDialog(null, "Item adicionado com sucesso");
                } else {
                    JOptionPane.showMessageDialog(null, "Preencher os campos obrigat√≥rios");
                }
            } else {
                JOptionPane.showMessageDialog(null, "N√£o foi poss√≠evel cadastrar item, campo ID preenchido");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        // BOT√ÉO EDITAR
        try {
            if (!jTextField2.getText().isEmpty()) {
                Item item = new Item();
                String nome = jTextField1.getText();
                int idItem = Integer.parseInt(jTextField2.getText());
                Double preco = Double.parseDouble(jTextField3.getText());
                String descricao = jTextArea1.getText();

                if (!nome.isEmpty()) {
                    item.set(nome, descricao, preco, true);
                    item.setIdItem(idItem);
                    ItemDAO dao = new ItemDAO();
                    dao.alterarItem(item);
                    limparCampos();
                    JOptionPane.showMessageDialog(null, "Item alterado com sucesso");
                } else {
                    JOptionPane.showMessageDialog(null, "Preencher os campos obrigat√≥rios");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Necess√°rio selecionar uma linha");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

