    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        // BOT√ÉO CONSULTAR
        try {
            String nome = jTextField1.getText();
            String idItem = jTextField2.getText();
            ArrayList<Item> itens = new ArrayList<>();

            ItemDAO dao = new ItemDAO();

            if (!nome.isEmpty()) {
                itens = dao.consultarItens(nome);
            } else if (!idItem.isEmpty()) {
                int idItemNum = Integer.parseInt(idItem);
                Item item = dao.getItem(idItemNum);
                itens.add(item);
            } else {
                itens = dao.listarItens();
            }

            modelo.setNumRows(0);

            for (Item item : itens) {
                Object[] dados = {item.getIdItem(), item.getNome(),
                    item.getPreco(), item.getDescricao()};
                modelo.addRow(dados);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Somente n√∫meros no campo ID");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        try {
            if (jTable1.getSelectedRow() != -1) {
                int idItem = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
                ItemDAO dao = new ItemDAO();
                Item item = dao.getItem(idItem);
                jTextField1.setText(item.getNome()); //NOME
                jTextField2.setText(String.valueOf(item.getIdItem())); //ID ITEM
                jTextField3.setText(String.valueOf(item.getPreco())); //PRE√áO
                jTextArea1.setText(item.getDescricao()); //DESCRICAO
                jTextField2.setEditable(false);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Falha ao contactar Banco de Dados. Favor entrar em contato com o desenvolvedor");
        }
    }//GEN-LAST:event_jTable1MouseClicked

